package alura_basic;

public class for_test {
    public static void main(String[] args) {
         for (int counter = 0; counter <= 10; counter++) {
             System.out.println(counter);

             // for( var; condition; incrementation)
             // ([first] is readied just one time;
             // [second] if true (execute the code below) false break;
             // [third] read just if the second is true)
         }
         // for works just on its own {...}
    }
}
